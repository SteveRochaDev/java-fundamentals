// Topic: Variables
// Description: Demonstrates basic variable types in Java

public class VariablesExample {

    public static void main(String[] args) {

        // Primitive data types
        int age = 25;          // integer
        double height = 1.80;  // decimal number
        boolean isStudent = true;

        // Reference type
        String name = "Steve";

        // Output
        System.out.println(name + " is " + age + " years old.");
    }
}