// Exercise: Largest of Two Numbers
// Level: Easy
// Task: Find the largest of two numbers

public class LargestOfTwo {

    public static void main(String[] args) {

        int a = 15;
        int b = 25;

        if (a > b) {
            System.out.println("Largest: " + a);
        } else {
            System.out.println("Largest: " + b);
        }
    }
}