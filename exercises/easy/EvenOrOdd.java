// Exercise: Even or Odd
// Level: Easy
// Task: Check if a number is even or odd

public class EvenOrOdd {

    public static void main(String[] args) {

        int number = 7;

        if (number % 2 == 0) {
            System.out.println("Even");
        } else {
            System.out.println("Odd");
        }
    }
}