// Exercise: Positive, Negative or Zero
// Level: Easy

public class NumberCheck {

    public static void main(String[] args) {

        int number = -5;

        if (number > 0) {
            System.out.println("Positive");
        } else if (number < 0) {
            System.out.println("Negative");
        } else {
            System.out.println("Zero");
        }
    }
}